export type QuestionType = "short_text" | "long_text" | "single" | "multi";

export type MapTo =
  | "name"
  | "position"
  | "org"
  | "location"
  | "contact"
  | "pilot"
  | "results"
  | "followup"
  | "training";

export interface Question {
  id: string;
  label: string;
  type: QuestionType;
  options?: string[];
  required?: boolean;
  // Маппинг ответа на колонку таблицы / производный булев флаг
  mapTo?: MapTo;
  // Значения, при которых булев флаг (pilot/results/followup/training) = true
  trueValues?: string[];
  placeholder?: string;
}

export interface SurveyDef {
  type: "agro" | "industry";
  title: string;
  intro: string;
  questions: Question[];
}

